#include "calc.h"

/**
 * op_add - adds two integers
 * @a: integer to be added
 * @b: integer to be added
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	op_t;

	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: number to be subtracted
 * @b: number to be subtracted
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	op_t;

	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: number to be multiplied
 * @b: number to be multiplied
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	op_t;

	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: number to be divided
 * @b: number to be divided
 *
 * Return: result of the division of a by b
 */

int op_div(int a, int b)
{
	op_t;

	return (a / b);
}

/**
 * op_mod - gives the remainder of a division
 * @a: number to be divided
 * @b: number to be divided
 *
 * Return: remainder of the division of a by b
 */

int op_mod(int a, int b)
{
	op_t;

	return (a % b);
}
